package com.personal.cv.entity;

public class EnjoyInfo {
    private long enjoyId;
    private String enjoyKey;
    private String ip;

    public long getEnjoyId() {
        return enjoyId;
    }

    public void setEnjoyId(long enjoyId) {
        this.enjoyId = enjoyId;
    }

    public String getEnjoyKey() {
        return enjoyKey;
    }

    public void setEnjoyKey(String enjoyKey) {
        this.enjoyKey = enjoyKey;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }
}
